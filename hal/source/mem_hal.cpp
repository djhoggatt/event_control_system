    HeapInfo get_heap_info()
    {
        HeapInfo ret_val;

        if (mem_hals[hal::platform()] != nullptr)
        {
            ret_val = mem_hals[hal::platform()]->get_heap_info();
        }

        return ret_val;
    }

    uint8_t *get_stack_pointer()
    {
        uint8_t *stack_pointer = nullptr;

        if (mem_hals[hal::platform()] != nullptr)
        {
            stack_pointer = mem_hals[hal::platform()]->get_stack_pointer();
        }

        return stack_pointer;
    }


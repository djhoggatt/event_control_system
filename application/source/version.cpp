    int32_t get_param(settings::ID setting, uintptr_t value)
    {
        error::Error ret_val = error::NoError;

        switch (setting)
        {
            case settings::ID::VersionString:
                *((char **)value) = (char *)VERSION_STR;
                break;

            default:
                ret_val = error::IDNotFound;
                break;
        }

        return ret_val;
    }

    int32_t set_param(settings::ID setting, uintptr_t value, bool bootup)
    {
        UNUSED(bootup);
        UNUSED(setting);
        UNUSED(value);

        return (int32_t)error::WriteFailed;
    }


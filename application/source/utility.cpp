    /// @brief Determines if the system is little endian.
    /// @return True if little endian, otherwise false.
    ///
    bool is_little_endian()
    {
        uint16_t temp = 0x1234;

        return (*((uint8_t *)(&temp)) != 0x12);
    }

    /// @brief Swaps the byte order in the given data.
    /// @param data Data to swap.
    /// @param length Length of the data.
    /// @param swap True to swap (allows for conditional swapping).
    ///
    void swap_byte_order(uint8_t *data, uint32_t length, bool swap)
    {
        REQUIRE(data != nullptr, error::InvalidPointer);

        if (!swap)
        {
            return;
        }

        for (uint32_t i = 0; i < (length / 2); i++)
        {
            uint8_t temp           = data[i];
            data[i]                = data[(length - i) - 1];
            data[(length - i) - 1] = temp;
        }
    }


    /// @brief Initializes all relevant modules. Should be called before the scheduler starts.
    ///
    void init()
    {
        REENTRY_GUARD();

        task::init();

        setvbuf(stdout, NULL, _IONBF, 0);

        event::init();

        hal::init();

        settings::init();
    }

    /// @brief Opens all relevant modules. Should be called after the scheduler starts.
    ///
